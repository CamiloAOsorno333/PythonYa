"""Calcular el sueldo mensual de un operario conociendo
la cantidad de horas trabajadas y el valor por hora."""

horas=int(input("Por favor ingrese la cantidad de horas laboradas" + '\n'))

valor=int(input("Por favor ingrese el valor de la hora" + '\n'))

sueldo= horas * valor

print("El total del sueldo a pagar es= ", sueldo )