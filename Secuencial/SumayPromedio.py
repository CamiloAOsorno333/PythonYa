
"""Realizar un programa que lea cuatro valores numéricos e informar su suma y promedio."""

n1=int(input("Por favor ingrese el Nro. 1" + '\n'))
n2=int(input("Por favor ingrese el Nro. 2" + '\n'))
n3=int(input("Por favor ingrese el Nro. 3" + '\n'))
n4=int(input("Por favor ingrese el Nro. 4" + '\n'))

suma= n1 + n2 + n3 + n4

promedio= suma / 4

print("La suma de los números es= " , suma, '\n' , "El promedio de los números es= " , promedio)
