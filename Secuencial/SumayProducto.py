"""Escribir un programa en el cual se ingresen cuatro números, calcular e informar la suma
de los dos primeros  y el producto del tercero y el cuarto."""

n1=int(input("por favor ingrese el Nro. 1 " + '\n'))
n2=int(input("por favor ingrese el Nro. 2 " + '\n'))
n3=int(input("por favor ingrese el Nro. 3 " + '\n'))
n4=int(input("por favor ingrese el Nro. 4 " + '\n'))

suma= n1 + n2

producto= n3 + n4

print("Las suma de los primeros dos números es= " , suma)

print("El producto de los segundos dos números es= " , producto, '\n')

