##upper() : devuelve una cadena de caracteres convertida todos sus caracteres a mayúsculas.
##lower() : devuelve una cadena de caracteres convertida todos sus caracteres a minúsculas.
##capitalize() : devuelve una cadena de caracteres convertida a mayúscula solo su primer caracter y todos los demás a
# minúsculas.

"""Inicializar un string con la cadena "mAriA" luego llamar a sus métodos upper(), lower() y capitalize(),
guardar los datos retornados en otros string y mostrarlos por pantalla."""

nombre="mAriA"

input(nombre.upper())

input(nombre.lower())

