"""Realizar un programa que solicite ingresar dos
números distintos y muestre por pantalla el mayor de ellos."""

n1=int(input("Por favo ingrese el Nro. 1= " + '\n'))
n2=int(input("Por favo ingrese el Nro. 2= " + '\n'))

if n1 > n2:
    print("El número mayor es = " , n1)
else:
    print("El número mayor es = " , n2)